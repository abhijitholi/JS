var word = 'hello world';

// indexOf
console.log(word.indexOf("o"));// Output 4
console.log(word.indexOf("h"));// Output 0
console.log(word.indexOf(" "));// Output 5
console.log(word.indexOf("llo"));// Output 2
console.log(word.indexOf("A"));// Output -1

// charAt
console.log(word.charAt(0));// Output -h
console.log(word.charAt(1));// Output -e
console.log(word.charAt(10));// Output -l
console.log(word.charAt(15));// Output - 
